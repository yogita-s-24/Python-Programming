# list are store value in [] brakets

#List
students = ['Aachal','Aarav','Avinash','Dolly','Deva']
print(students)
#Output :['Aachal', 'Aarav', 'Avinash', 'Dolly', 'Deva']


#Slicing on List
#Example 1
students = ['Aachal','Aarav','Avinash','Dolly','Deva']
print(students[0:4])
#Output :['Aarav', 'Avinash', 'Dolly']


#Example 2
students = ['Aachal','Aarav','Avinash','Dolly','Deva']
print(students[4:])
#Output :['Deva']


#Example 3
students = ['Aachal','Aarav','Avinash','Dolly','Deva']
print(students[-2])
#Output : Dolly  (It is delete from last)